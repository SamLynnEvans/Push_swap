#include "libft.h"

void	ft_strdebug(char *s, char *name)
{
	write(1, "\x1b[33m ***DEBUG*** ", 19);
	write(1, "\x1b[0m", 5);
	ft_putstr(name);
	write(1, " is '", 5);
	ft_putstr(s);
	write(1, "'\n", 2);
}
